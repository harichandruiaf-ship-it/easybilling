/**
 * Merges all documentation Markdown under docs/ into one PDF (EasyBilling_Documentation.pdf).
 * Requires: npm install (devDependency md-to-pdf).
 */
import { readFileSync } from "fs";
import { dirname, join } from "path";
import { fileURLToPath } from "url";
import { createRequire } from "module";

const require = createRequire(import.meta.url);
const { mdToPdf } = require("md-to-pdf");

const __dirname = dirname(fileURLToPath(import.meta.url));
const rootDir = join(__dirname, "..");
const docsDir = join(rootDir, "docs");

/** Order matches docs/TABLE_OF_CONTENTS.md */
const REL_PATHS = [
  "README.md",
  "TABLE_OF_CONTENTS.md",
  "USER_MANUAL.md",
  "SCREEN_INVENTORY.md",
  "APPENDIX_BUSINESS_LOGIC.md",
  "APPENDIX_GLOSSARY.md",
  "REVIEW_CHECKLIST.md",
  "SOP/README.md",
  "SOP/01-first-run-and-settings.md",
  "SOP/02-create-and-generate-invoice.md",
  "SOP/03-record-payment-opening-and-fifo.md",
  "SOP/04-convert-quick-order-to-invoice.md",
  "SOP/05-edit-invoice.md",
  "SOP/06-month-end-gst-review-reports.md",
  "SOP/07-revoke-payment.md",
  "SOP/08-customer-opening-balance.md",
];

const PREAMBLE = `# Easy Billing — full documentation (PDF bundle)

_This file is generated by \`npm run docs:pdf\`. Edit the Markdown sources in \`docs/\` and rebuild._

`;

function buildMarkdown() {
  let out = PREAMBLE;
  for (const rel of REL_PATHS) {
    const abs = join(docsDir, rel);
    const chunk = readFileSync(abs, "utf8");
    out += `\n\n---\n\n## Source: \`${rel.replace(/\\/g, "/")}\`\n\n${chunk}`;
  }
  return out;
}

const dest = join(docsDir, "EasyBilling_Documentation.pdf");

await mdToPdf(
  { content: buildMarkdown() },
  {
    dest,
    basedir: docsDir,
    document_title: "Easy Billing Documentation",
    pdf_options: {
      format: "A4",
      margin: "14mm",
      printBackground: true,
    },
  }
);

console.log(`Wrote ${dest}`);
